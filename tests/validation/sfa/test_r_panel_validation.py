"""
Validation of panel SFA models against R frontier package.

This module compares PanelBox estimates with R frontier package for:
1. Pitt & Lee (1981) - timeEffect=FALSE
2. Battese & Coelli (1992) - timeEffect=TRUE

R frontier package:
    library(frontier)
    sfa(..., timeEffect=FALSE/TRUE)

Tolerance for comparisons:
- Coefficients (β): ± 1e-4
- η parameter: ± 1e-3
- Efficiencies: ± 1e-3
"""

