class TestRValidation:
    """Test PanelBox against R frontier package."""

    @pytest.mark.xfail(
        reason="Pitt-Lee optimization converges to different local optimum than R frontier (sigma_u near 0 vs 0.2)",
        strict=False,
    )
    def test_pitt_lee_vs_r(self):
        """Test Pitt-Lee model against R frontier (timeEffect=FALSE)."""

        from scipy.optimize import minimize

        from panelbox.frontier.panel_likelihoods import loglik_pitt_lee_half_normal

        # Load R-generated data and results
        test_dir = Path(__file__).parent

        # Load panel data
        panel_data = pd.read_csv(test_dir / "r_panel_data.csv")

        # Load R results
        import subprocess

        # Read RDS file using R
        r_script = f"""
        results <- readRDS('{test_dir / "r_pitt_lee_results.rds"}')
        cat(results$beta[1], "\\n")  # intercept
        cat(results$beta[2], "\\n")  # x1
        cat(results$beta[3], "\\n")  # x2
        cat(results$sigma_v, "\\n")
        cat(results$sigma_u, "\\n")
        cat(results$loglik, "\\n")
        """

        result_r = subprocess.run(["Rscript", "-e", r_script], capture_output=True, text=True)

        r_values = [float(x) for x in result_r.stdout.strip().split("\n")]
        r_results = {
            "beta_0": r_values[0],
            "beta_1": r_values[1],
            "beta_2": r_values[2],
            "sigma_v": r_values[3],
            "sigma_u": r_values[4],
            "loglik": r_values[5],
        }

        # Prepare data for PanelBox
        y = panel_data["output"].values
        X = np.column_stack(
            [np.ones(len(panel_data)), panel_data["x1"].values, panel_data["x2"].values]
        )
        entity_id = panel_data["entity"].values - 1  # R uses 1-indexed
        time_id = panel_data["time"].values - 1

        # Initial values from OLS
        beta_init = np.linalg.lstsq(X, y, rcond=None)[0]
        resid = y - X @ beta_init
        sigma_sq_init = np.var(resid)

        theta_init = np.concatenate(
            [beta_init, [np.log(sigma_sq_init / 2)], [np.log(sigma_sq_init / 2)]]
        )

        # Optimize
        def negloglik(theta):
            ll = loglik_pitt_lee_half_normal(
                theta,
                y,
                X,
                entity_id,
                time_id,
                sign=1,  # sign=1 for production (y = Xβ + v - u)
            )
            return -ll if np.isfinite(ll) else 1e10

        result = minimize(
            negloglik, theta_init, method="L-BFGS-B", options={"maxiter": 1000, "ftol": 1e-9}
        )

        assert result.success, f"Optimization failed: {result.message}"

        # Extract estimates
        k = X.shape[1]
        beta_hat = result.x[:k]
        sigma_v_hat = np.sqrt(np.exp(result.x[k]))
        sigma_u_hat = np.sqrt(np.exp(result.x[k + 1]))
        loglik_hat = -result.fun

        # Compare with R (tolerance ± 1e-4 as per spec)
        print("\nPitt-Lee Validation:")
        print(
            f"  β₀: PanelBox={beta_hat[0]:.6f}, R={r_results['beta_0']:.6f}, diff={abs(beta_hat[0] - r_results['beta_0']):.2e}"
        )
        print(
            f"  β₁: PanelBox={beta_hat[1]:.6f}, R={r_results['beta_1']:.6f}, diff={abs(beta_hat[1] - r_results['beta_1']):.2e}"
        )
        print(
            f"  β₂: PanelBox={beta_hat[2]:.6f}, R={r_results['beta_2']:.6f}, diff={abs(beta_hat[2] - r_results['beta_2']):.2e}"
        )
        print(
            f"  σᵥ: PanelBox={sigma_v_hat:.6f}, R={r_results['sigma_v']:.6f}, diff={abs(sigma_v_hat - r_results['sigma_v']):.2e}"
        )
        print(
            f"  σᵤ: PanelBox={sigma_u_hat:.6f}, R={r_results['sigma_u']:.6f}, diff={abs(sigma_u_hat - r_results['sigma_u']):.2e}"
        )
        print(
            f"  loglik: PanelBox={loglik_hat:.4f}, R={r_results['loglik']:.4f}, diff={abs(loglik_hat - r_results['loglik']):.2e}"
        )

        # Assertions with tolerance
        tol = 1e-4
        assert np.abs(beta_hat[0] - r_results["beta_0"]) < tol, "β₀ difference too large"
        assert np.abs(beta_hat[1] - r_results["beta_1"]) < tol, "β₁ difference too large"
        assert np.abs(beta_hat[2] - r_results["beta_2"]) < tol, "β₂ difference too large"
        assert np.abs(sigma_v_hat - r_results["sigma_v"]) < 1e-3, "σᵥ difference too large"
        assert np.abs(sigma_u_hat - r_results["sigma_u"]) < 1e-3, "σᵤ difference too large"

    @pytest.mark.skip(reason="Requires R frontier package and real data")
    def test_bc92_vs_r(self, rice_data, r_bc92_results):
        """Test BC92 model against R frontier (timeEffect=TRUE)."""
        from scipy.optimize import minimize

        from panelbox.frontier.panel_likelihoods import loglik_battese_coelli_92

        # Prepare data
        y = rice_data["output"].values
        X = np.column_stack(
            [
                np.ones(len(rice_data)),
                np.log(rice_data["area"].values),
                np.log(rice_data["labor"].values),
                np.log(rice_data["fertilizer"].values),
            ]
        )
        entity_id = rice_data["farm"].values
        time_id = rice_data["year"].values

        # Initial values
        from sklearn.linear_model import LinearRegression

        ols = LinearRegression(fit_intercept=False)
        ols.fit(X, y)
        beta_init = ols.coef_
        resid = y - X @ beta_init
        sigma_sq_init = np.var(resid)

        theta_init = np.concatenate(
            [
                beta_init,
                [np.log(sigma_sq_init / 2)],
                [np.log(sigma_sq_init / 2)],
                [0.0],  # mu
                [0.01],  # eta
            ]
        )

        # Optimize
        def negloglik(theta):
            ll = loglik_battese_coelli_92(theta, y, X, entity_id, time_id, sign=1)
            return -ll if np.isfinite(ll) else 1e10

        result = minimize(negloglik, theta_init, method="L-BFGS-B")

        if result.success:
            k = X.shape[1]
            beta_hat = result.x[:k]
            sigma_v_hat = np.sqrt(np.exp(result.x[k]))
            sigma_u_hat = np.sqrt(np.exp(result.x[k + 1]))
            eta_hat = result.x[k + 3]

            # Compare with R
            assert np.abs(beta_hat[0] - r_bc92_results["beta_intercept"]) < 1e-3
            assert np.abs(beta_hat[1] - r_bc92_results["beta_area"]) < 1e-3
            assert np.abs(sigma_v_hat - r_bc92_results["sigma_v"]) < 1e-3
            assert np.abs(sigma_u_hat - r_bc92_results["sigma_u"]) < 1e-3
            assert np.abs(eta_hat - r_bc92_results["eta"]) < 1e-2


