"""
Validation tests for Panel VECM estimation against R implementation (urca + vars packages).

This test suite validates PanelBox's Panel VECM implementation by comparing
with R's urca::ca.jo + vars::vec2var workflow.

NOTE: urca::ca.jo is for time series (not panels). We validate the methodology by
comparing results for individual entities. The panel implementation aggregates across
entities following Johansen methodology.
"""

import json
import subprocess
from pathlib import Path

import numpy as np
import pandas as pd
import pytest

from panelbox.var import PanelVARData
from panelbox.var.vecm import PanelVECM

pytestmark = pytest.mark.r_validation


@pytest.fixture(scope="module")
def r_validation_results():
    """
    Run R validation script for VECM estimation and return results.

    Returns
    -------
    dict
        Dictionary containing R VECM estimation results including:
        - beta: cointegrating vector
        - A1, A2: VAR coefficient matrices (from VECM->VAR conversion)
        - Sigma: residual covariance
    """
    # Path to R script
    script_path = Path(__file__).parent / "test_vecm_estimation_vs_r.R"

    # Run R script
    result = subprocess.run(
        ["Rscript", str(script_path)], capture_output=True, text=True, timeout=120
    )

    if result.returncode != 0:
        pytest.skip(f"R script failed: {result.stderr}")

    # Print R output for debugging
    if result.stdout:
        print("\nR Output:")
        print(result.stdout)

    # Load results
    results_path = Path("/tmp/vecm_estimation_r_results.json")
    if not results_path.exists():
        pytest.skip("R results file not found")

    with open(results_path) as f:
        r_results = json.load(f)

    return r_results


