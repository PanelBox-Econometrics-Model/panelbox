@pytest.fixture(scope="module")
def python_rank_results_entity1(panel_data):
    """
    Run rank test on first entity only (for comparison with R ca.jo).

    Parameters
    ----------
    panel_data : pd.DataFrame
        Full panel data

    Returns
    -------
    RankSelectionResult
        Rank selection results from PanelBox for entity 1
    """
    # Extract entity 1 only
    entity1_data = panel_data[panel_data["entity"] == 1].copy()

    # Create PanelVARData (treating as panel with N=1)
    pvar_data = PanelVARData(
        data=entity1_data,
        endog_vars=["y1", "y2", "y3"],
        entity_col="entity",
        time_col="time",
        lags=2,  # VECM(1) = VAR(2) in levels
        trend="none",
    )

    # Run rank test
    rank_test = CointegrationRankTest(pvar_data)
    results = rank_test.test_rank()

    return results


